    // Checks that phrase is correct
    function CheckPhrase($phrase) {
	unset($this->phrase);
	if ($phrase) { $this->phrase = $phrase; }
	$words = $this->_Prepare();		// Preparing phrase for checking

	// Checking each word in phrase, if found any foul word,
	//  we think that all phrase is bad
	if (is_array($words)) {
	    foreach($words as $word) {
		$this->word = $word;
		if (!$this->CheckWord()) { return(false); }
	    }
	}
	
	return(true);
    }

    function CheckWord($word = "") {
	if ($word) { $this->word = $word; }
	
	$this->word = $this->_PrepareWord($this->word);

	$this->status = true; // We are considering with presumption of innocence :)
	$fl = substr($this->word, 0, 1); // fl is the first letter - it is using to search rules by index later.

	// Accusing
	if ($this->status) { $this->_CheckRegexps($this->foulcore, true); }
	if ($this->status) { $this->_CheckRegexps($this->fouldata[$fl], true); }
	if ($this->status) { $this->_CheckRegexps($this->badphrases, true); }
	
	// Trying to appeal
	if (!$this->status) { $this->_CheckRegexps($this->excludescore, false); }
	if (!$this->status) { $this->_CheckRegexps($this->excludesdata[$fl], false); }
	
	return($this->status);
    }

    function GetRule() {
	return($this->rule);
    }
    
    function GetWord() {
	return($this->word);
    }
	
    // Preparing the phrase for processing
    function _Prepare() {

	$phrase = $this->phrase;
	
	// Processing punctuation
	$phrase = preg_replace("/[\"\-\+\;\.\,\*\?\(\)]/", "", $phrase);
	$phrase = preg_replace("/[\!\:\_]/", " ", $phrase);

	// Splitting phrase by words
	unset($buffer);
	$words = explode(" ", $phrase);
	if (!is_array($words)) { return; }

	// If the word is bigger than 3 symbols or it is a preposition,
	// we adding it as is. In another case we merging it. It is need
	// to find fouls written with spaces or some punctuations between
	// the letters.	
	foreach ($words as $word) {
	    if ((strlen($word)<3) and (!preg_match("/(а[х]?)|(в)|([вмт]ы)|(д[ао])|(же)|(за)/", $word))) { $buffer .= $word; }
	    else {
		if ($buffer) { $out[] = $buffer; $buffer = ""; }
		$out[] = $word;
	    }
	}
	if ($buffer) { $out[] = $buffer; }
	return($out);
    }
    
    function _PrepareWord($word = "") {
    
	if (!$word) { $word = $this->word; }
    
	$word = strtolower($word);	// word must be in lower case
	$word = rtrim($word);	// Trimming
	
	// Processing similar letters
	$word = preg_replace("/&[Ee][Uu][Mm][Ll];/", "е", $word); // &euml processing
	$word = preg_replace("/&#203;/", "е", $word); // 203 code processing
	$word = preg_replace("/&[Cc][Ee][Nn][Tt];/", "с", $word); // "&cent" processing
	$word = preg_replace("/&#162;/", "с", $word); // 162 code processing
	$word = preg_replace("/&#120;/", "х", $word); // 120 code processing
	$word = preg_replace("/&#121;/", "у", $word); // 121 code processing
	$word = preg_replace("/\|\/\|/", "и", $word); // |/| as И

	$word = strtr($word, "gcxyaeopkhbtmnu", "дсхуаеоркнвтмпи");
	$word = strtr($word, "036", "озб");
	$word = strtr($word, "ёdiz", "едиз");

	// Processing jokes with letter-named numbers
	$word = preg_replace("/3[\.\,]14[\d]{0,}/", "пи", $word); // "3.14 as pi" processing
	$word = preg_replace("/2[\.\,]72/", "е", $word); // "2.72" as "e" processing

	// Processing punctuation
	$word = preg_replace("/[\"\-\+\;\.\,\*\?\(\)]/", "", $word);
	$word = preg_replace("/[\!\:\_]/", " ", $word);

	// Processing duplicates
	unset($post); 	// Temporary variable to keep difference of pre- and post-processed values
	while($word != $post) {
	    $post = $word;
	    $word = preg_replace("/(.)\\1\\1/", "\\1\\1", $word);
	}

	return($word);

    }
        
    function _CheckRegexps($regexps, $accuse) {
	if (!is_array($regexps)) { return; }
	foreach ($regexps as $regexp) {
	    if (preg_match("/".$regexp."/", $this->word)) {
		$this->rule = $regexp;
		if ($accuse) { $this->status = false; } else { $this->status = true; }
		break;
	    }
	}
    }
    
